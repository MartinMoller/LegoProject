package FunctionLayer;

/**
 *
 * @author martin
 */
public class Brick {
    
    private int height;
    private int length;

    public Brick(int height, int length) {
        this.height = height;
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getLength() {
        return length;
    }

    public void setLength(int length) {
        this.length = length;
    }

}
